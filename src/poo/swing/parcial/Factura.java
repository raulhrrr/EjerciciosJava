    private void buttonAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddProductActionPerformed

        try {
            String name = this.productName.getText();
            Integer quantity = Integer.valueOf(this.productQuantity.getText());
            Double price = Double.valueOf(this.productPrice.getText());

            Product producto = new Product(name, quantity, price);
            Factura.products.add(producto);
            
            this.cleanFields();
            this.productFeedback.setText(String.format("%s se ha añadido ", name));
        } catch (NumberFormatException e) {
            this.productFeedback.setText("Cantidad debe ser un entero, Precio debe ser un decimal");
        }

    }//GEN-LAST:event_buttonAddProductActionPerformed

