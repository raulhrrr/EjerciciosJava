    private void buttonGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGenerateActionPerformed
        Informe panel = new Informe();
        
        panel.loadCompanyInfo(this.companyName.getText(), this.companyAddress.getText(), this.companyEmail.getText(), this.companyPhone.getText(), this.companyId.getText());
        panel.loadClientInfo(this.clientName.getText(), this.clientAddress.getText(), this.clientEmail.getText(), this.clientPhone.getText(), this.clientId.getText());
        panel.loadProductsInfo(Factura.products);
        
        JFrame detalle = new JFrame();
        
        detalle.getContentPane().add(panel);
        detalle.setSize(450, 750);
        detalle.setResizable(false);
        detalle.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        detalle.setVisible(true);
    }//GEN-LAST:event_buttonGenerateActionPerformed

